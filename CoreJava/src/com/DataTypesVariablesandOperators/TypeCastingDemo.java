package com.DataTypesVariablesandOperators;

public class TypeCastingDemo {
	public static void main(String[] args) {
		 int a = 10;
	        double b = a;  
	        System.out.println("Implicit Casting: " + b);
	        
	        double x = 10.75;
	        int y = (int) x;  
	        System.out.println("Explicit Casting: " + y);
	}
}
